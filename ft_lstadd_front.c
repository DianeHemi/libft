#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (*alst == NULL)
	{
		*alst = new;
		return ;
	}
	if (new == NULL)
		return ;
	new->next = *alst;
	*alst = new;
}
